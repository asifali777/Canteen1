let a = 0;
// for item 1 
document.getElementById("plus1").addEventListener("click",()=>{
    a++;
    document.getElementById("num1").innerHTML = a;
})
document.getElementById("minus1").addEventListener("click",()=>{
    if (a>0) {
        a--;
        document.getElementById("num1").innerHTML = a;
    } else {
        return 0;
    }
});
